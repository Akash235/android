public class Contact {
    String name;
    String image;
    String phone;
    String task;
    String date;


    public Contact() {
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }

    public String getPhone() {
        return phone;
    }

    public String getTask() {
        return task;
    }

    public String getDate() {
        return date;
    }

}
